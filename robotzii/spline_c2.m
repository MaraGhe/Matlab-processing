function coef = spline_c2(x, y)
    n = length(x) - 1
    t = diff(x)
    N = 4*n
    b = zeros(N, 1)
    A = zeros(N, N)

    i1 = 1 : n
    
    % si_xi = yi
    A(i1 + ((1:4:N)-1)*N) = 1

    % s(n-1)_xn = yn
    A(n+1, N-3: N) = [1 t(n) t(n)^2 t(n)^3]

    b(1:n+1) = y'

    i1 = n+2 : 2*n
    i2 = 2*n+1 : 3*n-1
    i3 = 3*n : 4*n-2
    t2 = t.^2; t3 = t.^3
    T = length(t)
    
    % si_x(i+1) = s(i+1)_x(i+1)
    A(i1 + ((1:4:N-4)-1)*N) = 1
    A(i1 + ((2:4:N-3)-1)*N) = t(1:T-1)
    A(i1 + ((3:4:N-2)-1)*N) = t2(1:T-1)
    A(i1 + ((4:4:N-1)-1)*N) = t3(1:T-1)
    A(i1 + ((5:4:N)  -1)*N) = -1
    
    % si'_x(i+1) = s(i+1)'_x(i+1)
    A(i2 + ((2:4:N-3)-1)*N) = 1
    A(i2 + ((3:4:N-2)-1)*N) = 2*t(1:T-1)
    A(i2 + ((4:4:N-1)-1)*N) = 3*t2(1:T-1)
    A(i2 + ((6:4:N+1)-1)*N) = -1
    
    % si''_x(i+1) = s(i+1)''_x(i+1)
    A(i3 + ((3:4:N-2)-1)*N) = 2
    A(i3 + ((4:4:N-1)-1)*N) = 6*t(1:T-1)
    A(i3 + ((7:4:N+2)-1)*N) = -2

    % s0''_x0 = 0
    A(N-1, 3) = 2

    % s(n-1)''_xn = 0
    A(N, N-1: N) = [2 6*t(n)]

    coef = A \ b
end
